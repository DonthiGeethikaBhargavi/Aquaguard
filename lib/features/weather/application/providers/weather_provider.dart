import 'package:flutter_riverpod/flutter_riverpod.dart';

import '../../data/datasources/weather_remote_datasource.dart';

import '../mappers/weather_mapper.dart';

import '../../domain/models/weather_model.dart';

import '../../../../core/location/location_service.dart';

////////////////////////////////////////////////////////////
/// USER WEATHER PROVIDER
////////////////////////////////////////////////////////////

final userWeatherProvider = FutureProvider<WeatherModel>((ref) async {
  final ds = WeatherRemoteDataSource();

  try {
    ////////////////////////////////////////////////////////////
    /// GPS LOCATION
    ////////////////////////////////////////////////////////////

    final location = await LocationService().getCurrentLocation();

    ////////////////////////////////////////////////////////////
    /// FETCH WEATHER
    ////////////////////////////////////////////////////////////

    final raw = await ds.fetchWeather(
      lat: location.latitude,
      lng: location.longitude,
    );

    ////////////////////////////////////////////////////////////
    /// SAFE WEATHER EXTRACTION
    ////////////////////////////////////////////////////////////

    final weatherList = raw['weather'] as List?;

    final weatherData = (weatherList != null && weatherList.isNotEmpty)
        ? weatherList.first
        : null;

    ////////////////////////////////////////////////////////////
    /// DEBUG
    ////////////////////////////////////////////////////////////

    print(
      'LIVE WEATHER DESCRIPTION => '
      '${weatherData?['description'] ?? 'unknown'}',
    );

    ////////////////////////////////////////////////////////////
    /// MAP
    ////////////////////////////////////////////////////////////

    return mapWeather(raw);
  } catch (e, stack) {
    ////////////////////////////////////////////////////////////
    /// ERROR LOG
    ////////////////////////////////////////////////////////////

    print('Weather Provider Error => $e');

    print(stack);

    ////////////////////////////////////////////////////////////
    /// FALLBACK LOCATION
    /// Nellore Region
    ////////////////////////////////////////////////////////////

    final raw = await ds.fetchWeather(lat: 14.4426, lng: 79.9865);

    return mapWeather(raw);
  }
});
