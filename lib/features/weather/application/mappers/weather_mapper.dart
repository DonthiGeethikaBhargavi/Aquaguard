import '../../domain/models/weather_model.dart';

////////////////////////////////////////////////////////////
/// WEATHER TYPE
////////////////////////////////////////////////////////////

WeatherType _mapType(String description, String icon) {
  final isNight = icon.contains('n');

  final d = description.toLowerCase();

  ////////////////////////////////////////////////////////////
  /// RAIN
  ////////////////////////////////////////////////////////////

  if (d.contains('rain') ||
      d.contains('drizzle') ||
      d.contains('thunder') ||
      d.contains('shower')) {
    return WeatherType.rainy;
  }

  ////////////////////////////////////////////////////////////
  /// CLOUDS
  ////////////////////////////////////////////////////////////

  if (d.contains('cloud')) {
    return WeatherType.cloudy;
  }

  ////////////////////////////////////////////////////////////
  /// SNOW
  ////////////////////////////////////////////////////////////

  if (d.contains('snow')) {
    return WeatherType.snow;
  }

  ////////////////////////////////////////////////////////////
  /// CLEAR
  ////////////////////////////////////////////////////////////

  if (d.contains('clear')) {
    return isNight ? WeatherType.night : WeatherType.sunny;
  }

  return isNight ? WeatherType.night : WeatherType.sunny;
}

////////////////////////////////////////////////////////////
/// MAP WEATHER
////////////////////////////////////////////////////////////

WeatherModel mapWeather(Map<String, dynamic> data) {
  final weather = data['weather'][0];

  final main = data['main'];

  final sys = data['sys'];

  ////////////////////////////////////////////////////////////
  /// IMPORTANT
  ////////////////////////////////////////////////////////////

  final description = weather['description'].toString().toLowerCase();

  ////////////////////////////////////////////////////////////
  /// DEBUG
  ////////////////////////////////////////////////////////////

  print('LIVE WEATHER DESCRIPTION => $description');

  return WeatherModel(
    //////////////////////////////////////////////////////////
    /// TEMP
    //////////////////////////////////////////////////////////
    temp: (main['temp'] as num).toDouble(),

    feelsLike: (main['feels_like'] as num).toDouble(),

    humidity: main['humidity'],

    //////////////////////////////////////////////////////////
    /// WIND
    //////////////////////////////////////////////////////////
    windSpeed: (data['wind']['speed'] as num).toDouble(),

    windDeg: (data['wind']['deg'] as num).toDouble(),

    //////////////////////////////////////////////////////////
    /// CLOUDS
    //////////////////////////////////////////////////////////
    cloudCoverage: data['clouds']['all'] ?? 0,

    //////////////////////////////////////////////////////////
    /// CONDITION
    //////////////////////////////////////////////////////////
    condition: description,

    type: _mapType(description, weather['icon']),

    //////////////////////////////////////////////////////////
    /// TIME
    //////////////////////////////////////////////////////////
    sunrise: DateTime.fromMillisecondsSinceEpoch(sys['sunrise'] * 1000),

    sunset: DateTime.fromMillisecondsSinceEpoch(sys['sunset'] * 1000),
  );
}
